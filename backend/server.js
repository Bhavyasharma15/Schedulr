app.get('/api/admin/dashboard', async (req, res) => {
  try {
    // We fetch users whose is_admin is false and join with companies
    // Thus giving us access to company info AND the registered user's email
    const { data: usersInfo, error: cError } = await supabase
      .from('users')
      .select('*, companies(*)')
      .eq('is_admin', false);
      
    if (cError) throw cError;

    // Formatting for the frontend master table logic
    const companiesFormatted = usersInfo.map(u => ({
      id: u.companies.id, // we identify primarily by company_id for the UI grid toggles
      name: u.companies.name,
      email: u.email, // Use the user's email login here
      industry: u.companies.industry,
      payment_proof: u.companies.payment_proof,
      subscription_status: u.companies.subscription_status,
      created_at: u.companies.created_at
    }));

    const { data: meetings, error: mError } = await supabase
      .from('meetings')
      .select('*, companies(name)')
      .order('created_at', { ascending: false });
      
    if (mError) throw mError;
    
    const formattedMeetings = meetings.map(m => ({ ...m, companyName: m.companies?.name || 'Unknown' }));

    res.status(200).json({ success: true, companies: companiesFormatted, meetings: formattedMeetings });
  } catch (error) {
    res.status(500).json({ success: false, message: 'Failed to fetch admin data' });
  }
});

app.put('/api/admin/company/:id', async (req, res) => {
  try {
    const { action, status } = req.body; 
    let updatePayload = {};
    if (action === 'approve') updatePayload = { subscription_status: 'active', is_approved: true };
    else if (action === 'toggle') updatePayload = { subscription_status: status };
    else if (action === 'reject') {
      await supabase.from('companies').delete().eq('id', req.params.id); // Cascade deletes users
      return res.status(200).json({ success: true, message: 'Company rejected' });
    }

    const { error } = await supabase.from('companies').update(updatePayload).eq('id', req.params.id);
    if (error) throw error;

    res.status(200).json({ success: true, message: 'Company updated' });
  } catch (error) {
    res.status(500).json({ success: false, message: 'Failed to update company' });
  }
});

