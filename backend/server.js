app.post('/api/company/:companyId/availability', async (req, res) => {
  try {
    const { time_slot } = req.body;
    
    // Validate formatting (optional but safe)
    if (!time_slot) throw new Error("Time slot required");
    
    const { data, error } = await supabase
      .from('company_time_slots')
      .insert([{ company_id: req.params.companyId, time_slot }])
      .select().single();
      
    if (error) {
       if (error.code === '23505') { // Postgres distinctive UNIQUE violation code
         return res.status(400).json({ success: false, message: 'Time slot already exists!' });
       }
       throw error;
    }
    res.status(200).json({ success: true, slot: data });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message || 'Failed to add time slot' });
  }
});

