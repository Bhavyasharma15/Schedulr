app.get('/api/meetings/slots', async (req, res) => {
  try {
    const { company_id, date } = req.query;

    // First check if the date is explicitly blocked by the company
    const { data: blockedData, error: blockErr } = await supabase
      .from('company_blocked_dates')
      .select('blocked_date')
      .eq('company_id', company_id)
      .eq('blocked_date', date)
      .single();

    if (blockErr && blockErr.code !== 'PGRST116') { // PGRST116 = No rows found
       throw blockErr;
    }

    if (blockedData) {
       // The date is blocked, return immediately
       return res.status(200).json({ success: true, isBlocked: true, availableSlots: [] });
    }
    
    // Continue with getting available slots for an unblocked date
    const { data: offeredSlots, error: offErr } = await supabase
      .from('company_time_slots')
      .select('time_slot')
      .eq('company_id', company_id);
    if (offErr) throw offErr;
    const allCompanySlots = offeredSlots.map(s => s.time_slot);

    // Then, query which of those slots are already taken perfectly for that specific date
    const { data: taken, error } = await supabase
      .from('meetings')
      .select('time_slot')
      .eq('company_id', company_id)
      .eq('date', date)
      .in('status', ['pending', 'confirmed']);
      
    if (error) throw error;
    const bookedSlotsMap = taken.map(m => m.time_slot);
    
    // The available slots is the array of natively offered slots MINUS the booked ones
    const availableSlots = allCompanySlots.filter(slot => !bookedSlotsMap.includes(slot));
    
    res.status(200).json({ success: true, isBlocked: false, availableSlots });
  } catch (error) {
    res.status(500).json({ success: false, message: 'Failed to fetch time slots logic' });
  }
});

