app.post('/api/auth/register', async (req, res) => {
  try {
    const { 
      userName, userEmail, userPassword, 
      companyName, industry, phone, address, paymentDetails, auth_id 
    } = req.body;
    
    // Check if user already exists
    const { data: existingUser } = await supabase.from('users').select('*').eq('email', userEmail).single();
    if (existingUser) {
      return res.status(400).json({ success: false, message: 'Email already registered in Users database' });
    }

    // Step 1: Create Company Profile
    const { data: company, error: compError } = await supabase
      .from('companies')
      .insert([{ 
        name: companyName, industry, phone, address, 
        payment_proof: paymentDetails, 
        subscription_status: 'pending', is_approved: false 
      }])
      .select().single();

    if (compError) throw compError;

    // Step 2: Create User associated with the Company
    const { data: user, error: userError } = await supabase
      .from('users')
      .insert([{ 
        id: auth_id || undefined, // Store the secure Auth UUID generated by Supabase
        name: userName, email: userEmail, password: 'Managed by Supabase Auth', 
        is_admin: false, company_id: company.id 
      }])
      .select().single();

    if (userError) throw userError;

    // Optional: Pre-fill some default time slots for the company
    const defaultSlots = ['09:00 AM', '10:00 AM', '11:00 AM', '01:00 PM', '02:00 PM', '03:00 PM', '04:00 PM'];
    const slotPayloads = defaultSlots.map(t => ({ company_id: company.id, time_slot: t }));
    await supabase.from('company_time_slots').insert(slotPayloads);

    res.status(200).json({ success: true, user, company });
  } catch (error) {
    console.error('Registration Error:', error);
    res.status(500).json({ success: false, message: 'Failed to complete registration' });
  }
});

app.post('/api/auth/login', async (req, res) => {
  try {
    const { email, password, auth_id } = req.body;
    
    // Authenticate through the unified "users" table
    // If the frontend already verified the password via Supabase Auth, we look up by auth_id or email
    let query = supabase.from('users').select('*, companies(*)').eq('email', email);
    
    // Backwards compatibility for accounts created before Supabase Auth was added
    if (!auth_id) {
       query = query.eq('password', password); 
    }

    const { data: user, error } = await query.single();

    if (error || !user) {
      return res.status(400).json({ success: false, message: 'Invalid email or password.' });
    }

    if (user.is_admin) {
      return res.status(200).json({ success: true, role: 'admin', user });
    }
    
    res.status(200).json({ success: true, role: 'company', user: { ...user, company: user.companies } });
  } catch (error) {
    console.error('Login error:', error);
    res.status(500).json({ success: false, message: 'Login execution failed' });
  }
});


